function changeLanguage(lang) {
    // 1. Guardar preferencia
    localStorage.setItem('preferredLanguage', lang);
    
    // 2. Cambiar tipografía si es japonés
    if (lang === 'ja') {
        document.body.style.fontFamily = "'Noto Sans JP', 'Inter', sans-serif";
    } else {
        document.body.style.fontFamily = "'Inter', sans-serif";
    }

    // 3. Buscar y reemplazar textos
    const elements = document.querySelectorAll('[data-i18n]');
    elements.forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[lang] && translations[lang][key]) {
            el.innerHTML = translations[lang][key];
        }
    });

    // 4. Actualizar estado visual de los botones
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.classList.remove('active');
        if(btn.innerText.toLowerCase() === lang) btn.classList.add('active');
    });
}

// Cargar idioma al iniciar
document.addEventListener('DOMContentLoaded', () => {
    const savedLang = localStorage.getItem('preferredLanguage') || 'en';
    changeLanguage(savedLang);
});
